def hex_to_kml_color(hex_color: str) -> str:
    """Конвертирует '#rrggbb' в формат KML 'aabbggrr' (непрозрачный, alpha=ff)."""
    hex_digits = hex_color.lstrip("#")
    r, g, b = hex_digits[0:2], hex_digits[2:4], hex_digits[4:6]
    return f"ff{b}{g}{r}"


